int main(void)
{

  /* USER CODE BEGIN 1 */
  int x, y;
  uint16_t dummy = 0x00;
  uint8_t current_buffer = 0, next_buffer = 1;
  /* USER CODE END 1 */

  /* Enable I-Cache-------------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache-------------------------------------------------------------*/
  SCB_EnableDCache();

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_CRC_Init();
  MX_DMA2D_Init();
  MX_FMC_Init();
  SDRAM_Init();
  MX_I2C1_Init();
  MX_I2C3_Init();
  MX_LTDC_Init();
  MX_SPI2_Init();
  MX_TIM2_Init();
  MX_USART1_UART_Init();

  /* USER CODE BEGIN 2 */
  LEPTON_CS_HIGH();
  while(HAL_SPI_Transmit(&hspi2, (uint8_t *)&dummy, 1, 0xFFFFFFFF) != HAL_OK);
  for(y = 0; y < 240*320; y++)
      Display_layer0_buffer0[y] = Display_layer0_buffer1[y] = 0;
  for(y = 0; y < 272*480; y++)
      Display_layer1[y] = 0;
  TFT_LCD_EN();
  TFT_BL_ON();
  for(y = 0; y < 60; y++)
      for(x = 0; x < 80; x++)
          LeptonBuffer[0][y*80 + x] = LeptonBuffer[1][y*80 + x] = 0;
  
  LeptonSync();
  LEPTON_CS_LOW();
  while(HAL_SPI_Receive_DMA(&hspi2, (uint8_t *) Lepton_VoSPI, 164) != HAL_OK);

  for(x = 0; x < 322; x++)
    Display_layer1[15*480+x+79] = Display_layer1[256*480+x+79] = 0xFFFFFFFF;
  for(y = 0; y < 240; y++)
    Display_layer1[(y+16)*480+79] = Display_layer1[(y+16)*480+400] = 0xFFFFFFFF;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
    if(BufferStatus[0] == BUFFER_RDY_DISP)
    {
        BufferStatus[0] = BUFFER_BUSY_DISP;
        GenerateDisplayBuffer(0, next_buffer);
        if(next_buffer == 0)
            HAL_LTDC_SetAddress(&hltdc, (uint32_t) Display_layer0_buffer0, 0);
        else
            HAL_LTDC_SetAddress(&hltdc, (uint32_t) Display_layer0_buffer1, 0);
        BufferStatus[0] = BUFFER_FREE;
        current_buffer = next_buffer;
        next_buffer = (current_buffer + 1) % 2;
    }
    else if(BufferStatus[1] == BUFFER_RDY_DISP)
    {
        BufferStatus[1] = BUFFER_BUSY_DISP;
        GenerateDisplayBuffer(1, next_buffer);
        if(next_buffer == 0)
            HAL_LTDC_SetAddress(&hltdc, (uint32_t) Display_layer0_buffer0, 0);
        else
            HAL_LTDC_SetAddress(&hltdc, (uint32_t) Display_layer0_buffer1, 0);
        BufferStatus[1] = BUFFER_FREE;
        current_buffer = next_buffer;
        next_buffer = (current_buffer + 1) % 2;
    }
  }
  /* USER CODE END 3 */

}

